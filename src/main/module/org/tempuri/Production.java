package org.tempuri;

public class Production {
	
	public String orderno;
    public String dealername;
    public String dealerno;
    public String ordertime;
    public String chpname;
    public String chpcode;
    public String salenum;
    public String guige;
	

	public String getOrderno() {
		return orderno;
	}
	public void setOrderno(String orderno) {
		this.orderno = orderno;
	}
	public String getDealername() {
		return dealername;
	}
	public void setDealername(String dealername) {
		this.dealername = dealername;
	}
	public String getDealerno() {
		return dealerno;
	}
	public void setDealerno(String dealerno) {
		this.dealerno = dealerno;
	}
	public String getOrdertime() {
		return ordertime;
	}
	public void setOrdertime(String ordertime) {
		this.ordertime = ordertime;
	}
	public String getChpname() {
		return chpname;
	}
	public void setChpname(String chpname) {
		this.chpname = chpname;
	}
	public String getChpcode() {
		return chpcode;
	}
	public void setChpcode(String chpcode) {
		this.chpcode = chpcode;
	}
	public String getSalenum() {
		return salenum;
	}
	public void setSalenum(String salenum) {
		this.salenum = salenum;
	}
	public String getGuige() {
		return guige;
	}
	public void setGuige(String guige) {
		this.guige = guige;
	}
	
}
