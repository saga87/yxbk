package com.wkrj.data.bean;

public class WkrjData {

	private String data_id;
	private String data_name;
	private String data_parent_id;
	private String data_order;
	private String data_show;
	public String getData_name() {
		return data_name;
	}
	public void setData_name(String data_name) {
		this.data_name = data_name;
	}
	public String getData_id() {
		return data_id;
	}
	public void setData_id(String data_id) {
		this.data_id = data_id;
	}
	public String getData_parent_id() {
		return data_parent_id;
	}
	public void setData_parent_id(String data_parent_id) {
		this.data_parent_id = data_parent_id;
	}
	public String getData_order() {
		return data_order;
	}
	public void setData_order(String data_order) {
		this.data_order = data_order;
	}
	public String getData_show() {
		return data_show;
	}
	public void setData_show(String data_show) {
		this.data_show = data_show;
	}
	
}
